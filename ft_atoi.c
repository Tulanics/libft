#include "libft.h"

int	ft_atoi(const char *str)
{
	int			sign;
	long long	result;
	int			i;

	result = 0;
	sign = 1;
	i = 0;
	while (str[i] && (str[i] == '\f' || str[i] == '\t' || str[i] == ' '
			|| str[i] == '\n' || str[i] == '\r' || str[i] == '\v'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i++] == '-')
			sign = -1;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		if ((result * sign) > 2147483647)
			return (-1);
		else if ((result * sign) < -2147483648)
			return (0);
		else
			result = result * 10 + (str[i++] - '0');
	}
	return (result * sign);
}
