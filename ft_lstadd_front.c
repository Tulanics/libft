#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (lst && new)
	{
		new->next = *lst;
		*lst = new;
	}
	else if (!*lst)
	{
		*lst = new;
		return ;
	}
	else
	{
		return ;
	}
}
